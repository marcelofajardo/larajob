            'email' => request('email'),                   
            'password' => Hash::make(request('password')),
            'usertype'=>request('usertype'),
        ]);

        Company::create([
            'name' => request('name'),
            'slug' => str_slug(request('name')),
            'user_id' => $user->id,
        ]);
